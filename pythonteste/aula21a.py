def contador(i, f, p):
    """
    Faz uma contagem e mostra na tela.
    :param i: Início da contagem
    :param f: Fim da contagem
    :param p: Passo da contagem
    :return: sem retorno
    Função criada por Gustavo Guanabara do canal CursoEmVideo
    """
    print(f'Início: {i} | Fim: {f} | Passo: {p}')


